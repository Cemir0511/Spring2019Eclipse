package day20WHileDoWhileForLoops;

public class ForLoopIntro {

	public static void main(String[] args) {
		

		for (int i =0; i<5; i++) {
			System.out.println("Okan Happy birthday");
		}
		
		
		//count from 1 to 10;
		for (int a = 1; a<=10; a ++) {
			System.out.print(a + " ");
			
		}
		
		System.out.println();
		
		//count form 10 to 1
		for (int b = 10; b>=0; b --) {
			System.out.print(b + " ");
		}
		
		int x = 5;
		while(x>0) {
			System.out.println(x + " ");
			x--;
		}
	}

}
